n1 = float(input("Digite o primeiro número: "))
n2 = float(input("Digite o segundo número: "))

soma = n1 + n2  
subtracao = n1 - n2
multiplicacao = n1 * n2
divisao = n1 / n2
resto = n1 % n2

print(f"{n1} + {n2} = {soma}")
print(f"{n1} - {n2} = {subtracao}")
print(f"{n1} * {n2} = {multiplicacao}")
print(f"{n1} / {n2} = {divisao}")
print(f"{n1} % {n2} = {resto}")
print(f"O resto da divisão de {n1} por {n2} é {resto}" )


